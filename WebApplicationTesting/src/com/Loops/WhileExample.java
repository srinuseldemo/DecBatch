package com.Loops;

public class WhileExample {
	
	public static void main(String[] args) {
		
		int a=1;
		String string="LiveTech";
		while(a<=5)
		{
			System.out.println(a+" "+string);
			a++;
		}
		
		System.out.println(" #################### ");
				
		String string1="Selenium";
		
		int y=-10;
		
		while(y<=0)
		{
			System.out.println(y+" "+string1);
			y=y+5;
		}
			
	}
}
