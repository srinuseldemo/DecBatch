package com.ConditionalStatements;

public class IfElseExample {
	
	public static void main(String[] args) {
		
		int var1=10;
		int var2=20;
		
		if(var1>var2) // checking for the condition
		{
			// True Block
			System.out.println(" Variable var1 is greater than varible var2");
			
		}
		else
		{
			// false Block
			System.out.println(" Variable var2 is greater than varible var1 ");
		}
	}

}
